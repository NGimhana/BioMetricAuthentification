    public void dataAddPressCollector(long keyPressTime){
        keyDataPress.add(keyPressTime);
    }
    public void dataAddReleaseCollector(long keyReleaseTime){
        keyDataRelease.add(keyReleaseTime);
    }
    public void AVGKeyTimeCounter(){
        for(int i=0;i<keyDataPress.size();i++){
            int charTime=(int)(keyDataRelease.get(i)-keyDataPress.get(i));
            int flyCharTime=(int)(flyKeyDataRelease.get(i)-flyKeyDataPress.get(i));
            keyData.add((charTime>0 ? charTime : -(charTime)));
                    
            keyData.add(flyCharTime > 0 ? flyCharTime : -flyCharTime);
        }
    }
    public void dataAddPressFlyCollector(long flyingTimePress){
        flyKeyDataPress.add(flyingTimePress);
    }
    public void dataAddReleaseFlyCollector(long flyingTimeRelease){
        flyKeyDataRelease.add(flyingTimeRelease);
    }
