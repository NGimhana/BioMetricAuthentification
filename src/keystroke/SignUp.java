    private void passwordTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordTextKeyPressed
        keyCount++;
        if(!stopWatch.running){
            stopWatch.start();
            charBeginTime = System.currentTimeMillis();   
            System.out.println("Char :"+evt.getKeyChar()+ " char Begin Time "+charBeginTime+
                    " Flying Time Pressed "+(stopWatch.getElapsedTimeinMilisec()>0 ? stopWatch.getElapsedTimeinMilisec():0));
            
            dataAddPressCollector(charBeginTime);
            dataAddPressFlyCollector(stopWatch.getElapsedTimeinMilisec()>0 ? stopWatch.getElapsedTimeinMilisec():0);
            
        }else{
            charBeginTime = System.currentTimeMillis();   
            stopWatch.split();        
            System.out.println("Char :"+evt.getKeyChar()+ " char Begin Time "+charBeginTime+
                    " Flying Time Pressed "+stopWatch.getElapsedTimeinMilisec());
            
            dataAddPressCollector(charBeginTime);
            dataAddPressFlyCollector(stopWatch.getElapsedTimeinMilisec());
            
            stopWatch.start();
        }     
    }//GEN-LAST:event_passwordTextKeyPressed

    private void passwordTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordTextKeyReleased
        charEndTime = System.currentTimeMillis();
        stopWatch.split();        
        System.out.println("Char "+evt.getKeyChar()+" Char End Time "+charEndTime+
                " Flying Time Released "+stopWatch.getElapsedTimeinMilisec());
        dataAddReleaseCollector(charEndTime);
        dataAddReleaseFlyCollector(stopWatch.getElapsedTimeinMilisec());
        stopWatch.start();    
    }//GEN-LAST:event_passwordTextKeyReleased

